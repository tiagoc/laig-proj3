void Pawn::render (float scale)
{
	float x = this->get_pos_x();
	float y = this->get_pos_y();
	glPushMatrix();
	glTranslated(y-1,-0.6,-x+1);
	glPushMatrix();
    glScaled(scale,scale,scale);
	glColor3f(color[0],color[1],color[2]);
    for(int i=0; i< FACES_COUNT*3; i+=3){
        glBegin(GL_TRIANGLES);
        glNormal3f(vertexs_pawn[indexes_pawn[i]].nx,vertexs_pawn[indexes_pawn[i]].ny,vertexs_pawn[indexes_pawn[i]].nz);
        glTexCoord2f(vertexs_pawn[indexes_pawn[i]].u,vertexs_pawn[indexes_pawn[i]].v);
        glVertex3f(vertexs_pawn[indexes_pawn[i]].x,vertexs_pawn[indexes_pawn[i]].y,vertexs_pawn[indexes_pawn[i]].z);

        glNormal3f(vertexs_pawn[indexes_pawn[i+1]].nx,vertexs_pawn[indexes_pawn[i+1]].ny,vertexs_pawn[indexes_pawn[i+1]].nz);
        glTexCoord2f(vertexs_pawn[indexes_pawn[i+1]].u,vertexs_pawn[indexes_pawn[i+1]].v);
        glVertex3f(vertexs_pawn[indexes_pawn[i+1]].x,vertexs_pawn[indexes_pawn[i+1]].y,vertexs_pawn[indexes_pawn[i+1]].z);

        glNormal3f(vertexs_pawn[indexes_pawn[i+2]].nx,vertexs_pawn[indexes_pawn[i+2]].ny,vertexs_pawn[indexes_pawn[i+2]].nz);
        glTexCoord2f(vertexs_pawn[indexes_pawn[i+2]].u,vertexs_pawn[indexes_pawn[i+2]].v);
        glVertex3f(vertexs_pawn[indexes_pawn[i+2]].x,vertexs_pawn[indexes_pawn[i+2]].y,vertexs_pawn[indexes_pawn[i+2]].z);
        glEnd();
    }
	glPopMatrix();
	glPopMatrix();
}